    private void initProvinsi() {
        listProvinsi = ConnectionManager.getDefaultEntityManager().createNamedQuery("Provinsi.findAll", Provinsi.class).getResultList();
        for (Provinsi provinsi : listProvinsi) {
            cbProvinsi.addItem(provinsi);
        }
    }

    private void initKota() {
        ((DefaultComboBoxModel) cbKabupaten.getModel()).removeAllElements();
        if (selectedProvinsi == null) return;
        for (Kota kota : selectedProvinsi.getKotaList()) {
            cbKabupaten.addItem(kota);
        }
    }

    private void initKecamatan() {
        ((DefaultComboBoxModel) cbKecamatan.getModel()).removeAllElements();
        if (selectedKota == null) return;
        for (Kecamatan kecamatan : selectedKota.getKecamatanList()) {
            cbKecamatan.addItem(kecamatan);
        }
    }

    private void initKelurahan() {
        ((DefaultComboBoxModel) cbKelurahan.getModel()).removeAllElements();
        if (selectedKecamatan == null) return;
        for (Kelurahan kelurahan : selectedKecamatan.getKelurahanList()) {
            cbKelurahan.addItem(kelurahan);
        }
    }

    private void initKodeSupplier() {
        String kode = ConnectionManager.getKodeSupplier();
        tfKodeSupplier.setText(kode);
    }
    
    private Supplier selectedSupplier = null;

    private void simpanSupplier(Boolean isUpdate) {
        try {
            if (selectedSupplier == null) {
                selectedSupplier = new Supplier();
            }
            if (selectedKelurahan == null) {
                throw new RuntimeException("Lokasi supplier belum diisi.");
            }
            String kode = new ValidatorUtil().isEmpty(tfKodeSupplier.getText(), "Kode Supplier");
            String nama = new ValidatorUtil().isEmpty(tfNama.getText(), "Nama Supplier");
            String alamat = new ValidatorUtil().isEmpty(taAlamat.getText(), "Alamat");
            String noTelepon = new ValidatorUtil().isNumber(tfNoTelepon.getText(), "No Telepon");
            String noFax = tfNoFax.getText();
            String email = tfEmail.getText();
            selectedSupplier.setKodeSupplier(kode);
            selectedSupplier.setNamaSupplier(nama);
            selectedSupplier.setKelurahan(selectedKelurahan);
            selectedSupplier.setAlamat(alamat);
            selectedSupplier.setNoTelepon(noTelepon);
            selectedSupplier.setNoFax(noFax);
            selectedSupplier.setEmail(email);
            
            ConnectionManager.getDefaultEntityManager().getTransaction().begin();
            ConnectionManager.getDefaultEntityManager().persist(selectedSupplier);
            ConnectionManager.getDefaultEntityManager().getTransaction().commit();
            JOptionPane.showMessageDialog(this, "Supplier berhasil disimpan..", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
            clear();
        } catch (Exception ex) {
            Logger.getLogger(EntriSupplierFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void clear(){
        initKodeSupplier();
        
        selectedSupplier = null;
        selectedProvinsi = null;
        selectedKota = null;
        selectedKecamatan = null;
        selectedKelurahan = null;
        
        tfNama.setText("");
        cbProvinsi.setSelectedIndex(0);
        tfNoTelepon.setText("");
        taAlamat.setText("");
        tfNoFax.setText("");
        tfEmail.setText("");
    }
    
    private void setSupplier(Supplier supplier){
        tfKodeSupplier.setText(supplier.getKodeSupplier());
        tfNama.setText(supplier.getNamaSupplier());
        tfNoTelepon.setText(supplier.getNoTelepon());
        taAlamat.setText(supplier.getAlamat());
        cbProvinsi.setSelectedItem(supplier.getKelurahan().getKecamatan().getKota().getProvinsi());
        cbKabupaten.setSelectedItem(supplier.getKelurahan().getKecamatan().getKota());
        cbKecamatan.setSelectedItem(supplier.getKelurahan().getKecamatan());
        cbKelurahan.setSelectedItem(supplier.getKelurahan());
        tfNoFax.setText(supplier.getNoFax());
        tfEmail.setText(supplier.getEmail());
    }

