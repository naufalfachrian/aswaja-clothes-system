package aswajaclothes.model.master;

/**
 *
 * @author littleflower
 */
public class PembelianBarangModel {
    
    private String kodePesanan;
    
    private String kodeBarang;
    
    private int quantity;
    
    private String tipeLengan;
    
    private String namaBarang;
    
    private String warna;
    
    private String area;
    
    private String ukuran;
    
    private int hargaHpp;
    
    private int hargaJualSatuan;

    public String getKodePesanan() {
        return kodePesanan;
    }

    public void setKodePesanan(String kodePesanan) {
        this.kodePesanan = kodePesanan;
    }

    public String getKodeBarang() {
        return kodeBarang;
    }

    public void setKodeBarang(String kodeBarang) {
        this.kodeBarang = kodeBarang;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getTipeLengan() {
        return tipeLengan;
    }

    public void setTipeLengan(String tipeLengan) {
        this.tipeLengan = tipeLengan;
    }

    public String getNamaBarang() {
        return namaBarang;
    }

    public void setNamaBarang(String namaBarang) {
        this.namaBarang = namaBarang;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getUkuran() {
        return ukuran;
    }

    public void setUkuran(String ukuran) {
        this.ukuran = ukuran;
    }

    public int getHargaHpp() {
        return hargaHpp;
    }

    public void setHargaHpp(int hargaHpp) {
        this.hargaHpp = hargaHpp;
    }

    public int getHargaJualSatuan() {
        return hargaJualSatuan;
    }

    public void setHargaJualSatuan(int hargaJualSatuan) {
        this.hargaJualSatuan = hargaJualSatuan;
    }
    
}
